CREATE DATABASE bancofilas
    WITH 
    OWNER = postgres
    ENCODING = 'UTF8'
    CONNECTION LIMIT = -1;

CREATE TABLE filanormal (
	nome VARCHAR(100) NOT NULL,
	senha BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
	datasenha DATE NOT NULL,
	horasenha TIME NOT NULL
);

CREATE TABLE filapreferencial (
	nome VARCHAR(100) NOT NULL,
	senha BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
	datasenha DATE NOT NULL,
	horasenha TIME NOT NULL
);

CREATE TABLE chamado (
	nome VARCHAR(50) NOT NULL,
	senha INTEGER NOT NULL,
	tiposenha VARCHAR(30)
);

CREATE TABLE usuario (
	email VARCHAR(30) PRIMARY KEY NOT NULL,
	senha VARCHAR(30) NOT NULL,
	nome VARCHAR(100) NOT NULL
);

